const fs = require('fs');
const path = require('path');

const {validationResult} = require('express-validator/check');

const PostShcema = require('../models/post');

exports.getPosts = (req, res, next) => {
    let totalItems;
    PostShcema.find()
        .then(posts => {
            if(!posts) {
                const error = new Error('Could not find posts.');
                error.status = 422;
                throw error;
            }

            res.status(200).json({
                message: 'Fetch posts successfully.',
                posts: posts,
                totalItems: totalItems
            })
        }).catch(err => {
            console.log("Get post err >>>", err);
            if(!err.status) {
                err.status = 500;
            }
            next();
        });
}


exports.createPost = (req, res, next) => {
    const errors = validationResult(req);
    if(!errors.isEmpty()) {
        const error = new Error('Validation failed, entered data is incorrect.');
        error.status = 422;
        throw error;
    }
    if(!req.file) {
        const error = new Error('No image provided.');
        error.status = 422;
        throw error;
    }

    const imageUrl = req.file.path.replace("\\" ,"/"); // this image file path generated by multer when storing image in folder 
    const {title, content} = req.body;
    const post = new PostShcema({
        title: title,
        content: content,
        imageUrl: imageUrl,
    });
    post.save()
        .then(user => {
            res.status(201).json({
                message: 'post created successfully!',
                post: post,
            });
        }).catch(err => {
            console.log("Create post err >>>", err);
            if(!err.status) {
                err.status = 500;
            }
            next();
        });
}


